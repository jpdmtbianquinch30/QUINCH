  static const String _prefKey = 'quinch_locale';
  String _locale = 'fr';

  String get locale => _locale;

  Future<void> initialize() async {
    final prefs = await SharedPreferences.getInstance();
    _locale = prefs.getString(_prefKey) ?? 'fr';
    notifyListeners();
  }

  Future<void> setLocale(String locale) async {
    if (!_translations.containsKey(locale)) return;
    _locale = locale;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_prefKey, locale);
    notifyListeners();
  }

  /// Get translated string by key. Falls back to French then key itself.
  String t(String key) {
    return _translations[_locale]?[key]
        ?? _translations['fr']?[key]
        ?? key;
  }

