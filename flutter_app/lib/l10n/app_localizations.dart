/// InheritedWidget so we can use AppLocalizations.of(context)
class LocalizationsProvider extends StatelessWidget {
  final AppLocalizations localizations;
  final Widget child;
  const LocalizationsProvider({super.key, required this.localizations, required this.child});

  @override
  Widget build(BuildContext context) {
    return _LocalizationsInherited(localizations: localizations, child: child);
  }
}

class _LocalizationsInherited extends InheritedWidget {
  final AppLocalizations localizations;
  const _LocalizationsInherited({required this.localizations, required super.child});

  @override
  bool updateShouldNotify(_LocalizationsInherited old) => old.localizations.locale != localizations.locale;
}
